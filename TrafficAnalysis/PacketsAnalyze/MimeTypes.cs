        /// <summary>
        /// Returns the mime type for the requested file extension. Returns
        /// the default application/octet-stream if the extension is not found.
        /// </summary>
        /// <param name="extension"></param>
        /// <returns></returns>
        public static String GetMIME(String extension)
        {
            return GetMIME(extension, MimeTypeDict["bin"]);
        }

        /// <summary>
        /// Returns the mime type for the requested file extension. Returns the
        /// specified defaultMimeType if the extension is not found.
        /// </summary>
        /// <param name="extension"></param>
        /// <param name="defaultMimeType"></param>
        /// <returns></returns>
        public static String GetMIME(String extension, String defaultMimeType)
        {
            if (extension.StartsWith("."))
                extension = extension.Remove(0, 1);

            if (MimeTypeDict.ContainsKey(extension))
                return MimeTypeDict[extension];
            else
                return defaultMimeType;
        }

        /// <summary>
        /// Returns the file extension for the requested mime type. Returns
        /// empty string if the mime type is not found.
        /// </summary>
        /// <param name="mime"></param>
        /// <returns></returns>
        public static string GetExt(string mime)
        {
            return GetExt(mime, "", true);
        }

        /// <summary>
        /// Returns the file extension for the requested mime type. Returns
        /// specified defaultExt if the mime type is not found.
        /// </summary>
        /// <param name="mime"></param>
        /// <param name="defaultExt"></param>
        /// <param name="dot">Indicate whether the returned extension should have a dot</param>
        /// <returns></returns>
        public static string GetExt(string mime, string defaultExt, bool dot = true)
        {
            if (ReverseDic.ContainsKey(mime))
                return (dot ? "." : "") + ReverseDic[mime];
            else
                return defaultExt;
        }

